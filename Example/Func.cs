namespace Example;

/// <summary>
/// Arithmetics function.
/// </summary>
public class Func
{
    /// <summary>
    /// Addition operation.
    /// </summary>
    /// <param name="a">Operand 1.</param>
    /// <param name="b">Operand 2.</param>
    /// <returns>Result.</returns>
    public static int Add(int a, int b) => a + b;

    /// <summary>
    /// Subtraction operation.
    /// </summary>
    /// <param name="a">Operand 1.</param>
    /// <param name="b">Operand 2.</param>
    /// <returns>Result.</returns>
    public static int Sub(int a, int b) => a - b;
}
